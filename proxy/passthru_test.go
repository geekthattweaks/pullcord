func getLandingResponder(t *testing.T) (*url.URL, *http.Server, error) {
	l, e := net.Listen("tcp", "")
	if e != nil {
		return nil, nil, e
	}

	a := l.Addr()

	u, e := url.Parse(fmt.Sprintf("http://%s/", a.String()))
	if e != nil {
		return nil, nil, e
	}

	s := &http.Server{
		Handler: new(util.LandingHandler),
	}

	go func(t *testing.T, s *http.Server, l net.Listener) {
		expectedErr := http.ErrServerClosed
		actualErr := s.Serve(l)
		require.Equal(
			t,
			expectedErr,
			actualErr,
			"Unable to serve HTTP required by tests",
		)
	}(t, s, l)

	return u, s, nil
}

// TestPassthru verifies that a Falcore RequestFilter generated by
// NewPassthruFilter will forward web traffic as expected.
func TestPassthru(t *testing.T) {
	request := httptest.NewRequest("GET", "http://localhost", nil)

	regex, err := regexp.Compile("Pullcord Landing Page")
	require.NoError(t, err)

	u, s, err := getLandingResponder(t)
	assert.NoError(t, err)
	defer s.Close()

	passthru := NewPassthruFilter(u)

	w := httptest.NewRecorder()
	passthru.ServeHTTP(w, request)
	response := w.Result()

	assert.Equal(t, 200, response.StatusCode)
	contents, err := ioutil.ReadAll(response.Body)
	assert.NoError(t, err)
	assert.True(t, regex.Match(contents))
}

